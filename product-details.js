function text(id, value) {
  const element = document.getElementById(id);
  if (element) element.textContent = value;
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

const siteOrigin = "https://acfinds.net";

function getAbsoluteUrl(path) {
  if (/^https?:\/\//i.test(path)) return path;
  return `${siteOrigin}${path.startsWith("/") ? path : `/${path}`}`;
}

function setHeadContent(selector, attribute, value) {
  let element = document.head.querySelector(selector);
  if (!element) {
    element = document.createElement(selector.startsWith("link") ? "link" : "meta");
    const nameMatch = selector.match(/\[name="([^"]+)"\]/);
    const propertyMatch = selector.match(/\[property="([^"]+)"\]/);
    const relMatch = selector.match(/\[rel="([^"]+)"\]/);
    if (nameMatch) element.setAttribute("name", nameMatch[1]);
    if (propertyMatch) element.setAttribute("property", propertyMatch[1]);
    if (relMatch) element.setAttribute("rel", relMatch[1]);
    document.head.appendChild(element);
  }
  element.setAttribute(attribute, value);
}

function updateSeoMeta({ title, description, url, image }) {
  const absoluteUrl = getAbsoluteUrl(url);
  const absoluteImage = getAbsoluteUrl(image);

  document.title = title;
  setHeadContent('meta[name="description"]', "content", description);
  setHeadContent('link[rel="canonical"]', "href", absoluteUrl);
  setHeadContent('meta[property="og:title"]', "content", title);
  setHeadContent('meta[property="og:description"]', "content", description);
  setHeadContent('meta[property="og:type"]', "content", "article");
  setHeadContent('meta[property="og:url"]', "content", absoluteUrl);
  setHeadContent('meta[property="og:image"]', "content", absoluteImage);
  setHeadContent('meta[name="twitter:card"]', "content", "summary_large_image");
  setHeadContent('meta[name="twitter:title"]', "content", title);
  setHeadContent('meta[name="twitter:description"]', "content", description);
  setHeadContent('meta[name="twitter:image"]', "content", absoluteImage);
}

function renderProduct() {
  const slug = window.location.pathname.split("/").filter(Boolean).pop();
  const product = productRows.find((row) => row.slug === slug) || productRows[0];
  const title = product.name + " QC | AllChinaBuy Spreadsheet Find";
  const description = `${product.copy} Check price range, fit notes, QC focus, and shipping context before opening the live source.`;
  updateSeoMeta({
    title,
    description,
    url: `/products/${product.slug}`,
    image: product.image
  });

  text("product-eyebrow", "AllChinaBuy " + product.category + " product row");
  text("product-title", product.name + " AllChinaBuy find.");
  text("product-description", product.copy);
  text("detail-category", product.category);
  text("detail-price", product.price);
  text("detail-status", product.status);
  text("detail-last", product.lastChecked);
  text("detail-qc", product.qc);
  text("detail-fit", product.fit);
  text("image-heading", product.name + " image and spreadsheet context");
  text("detail-tag", product.category);
  text("detail-name", product.name);
  text("detail-copy", product.copy);
  text("detail-price-copy", product.price);
  text("detail-fit-copy", product.fit);
  text("detail-qc-copy", product.qc);

  const source = document.getElementById("product-source");
  if (source) source.href = product.href;

  const image = document.getElementById("detail-image");
  if (image) {
    image.src = product.image;
    image.alt = product.alt;
  }

  const related = document.getElementById("related-products");
  if (related) {
    related.innerHTML = productRows
      .filter((row) => row.slug !== product.slug && row.category === product.category)
      .slice(0, 4)
      .map((row) => {
        return [
          "<article class=\"compare-card\">",
          "<h3><a href=\"/products/" + escapeHtml(row.slug) + "\">" + escapeHtml(row.name) + "</a></h3>",
          "<p>" + escapeHtml(row.copy) + "</p>",
          "<p class=\"mini-meta\"><span class=\"price-text\" translate=\"no\">" + escapeHtml(row.price) + "</span> - " + escapeHtml(row.status) + "</p>",
          "</article>",
        ].join("");
      })
      .join("");
  }
}

